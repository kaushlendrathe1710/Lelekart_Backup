// Helper function to get cities by state name
export const getCitiesByState = (stateName: string): string[] => {
  const state = indianStates.find((s) => s.name === stateName);
  return state ? state.cities.sort() : [];
};

// Helper function to get state by code
export const getStateByCode = (code: string): IndianState | undefined => {
  return indianStates.find((s) => s.code === code);
};

// Helper function to get all states sorted alphabetically
export const getSortedStates = (): IndianState[] => {
  return [...indianStates].sort((a, b) => a.name.localeCompare(b.name));
};

// Helper function to search cities by partial name
export const searchCitiesByState = (
  stateName: string,
  searchTerm: string
): string[] => {
  const cities = getCitiesByState(stateName);
  if (!searchTerm) return cities;
  return cities.filter((city) =>
    city.toLowerCase().includes(searchTerm.toLowerCase())
  );
};
